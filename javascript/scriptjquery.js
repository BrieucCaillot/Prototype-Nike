$(document).ready(function() {

	$(document).ready(function(){
			$("#wish").click(function(){
				$("#wish-liked").css("display", "block");
				$("#wish").css("display", "none");
			});
	});

	$(document).ready(function(){
			$("#wish-liked").click(function(){
				$("#wish").css("display", "block");
				$("#wish-liked").css("display", "none");
			});
	});

});






